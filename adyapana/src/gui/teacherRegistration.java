    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int row = jTable1.getSelectedRow();
        String tno = (String) teacherTableModel.getValueAt(row, 0);
        Object[] teacherData = teacherModel.getSelectedTeacherDataById(tno);

        jTextField5.setText((String) teacherData[0]);
        jTextField1.setText((String) teacherData[1]);
        jTextField2.setText((String) teacherData[2]);
        jComboBox1.setSelectedItem((String) teacherData[3]);

    }//GEN-LAST:event_jTable1MouseClicked

