    public void comboBox() {
         String query = "SELECT * FROM subjects";
        try {
           
            PreparedStatement psm = conn.prepareStatement(query);
            ResultSet rs = psm.executeQuery();

            while (rs.next()) {
                String sno = rs.getString("sub_no");
                jComboBox1.addItem(sno);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

